
public class HelloWorld {

	static void Sum(int a, int b) {
		int result = a + b;
		System.out.println(result);
	}

	static void Sub(int a, int b) {

		int result = a - b;
		System.out.println(result);

	}

	public static void main(String[] args) {

		Sum(10, 05);
		Sub(50, 25);

	}

}
